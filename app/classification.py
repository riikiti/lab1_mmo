def classification(x, y, metka):
    # Шаг обучения (скорость изменения весов)
    eta = 0.1
    # Начальные веса классификатора: свободный член (w[0]) и два коэффициента (w[1], w[2])
    w = [0, 0, 0]
    # Преобразование меток: 1 остаётся 1, все остальные заменяются на -1
    metka = [1 if m == 1 else -1 for m in metka]

    # Максимум 10,000 итераций обучения
    for j in range(10000):
        count = 0  # Счётчик ошибок за одну итерацию
        for i in range(len(x)):
            # Вычисляем выход классификатора для точки
            otvet = w[1] * x[i] + w[2] * y[i] + w[0]
            # Преобразуем выход в бинарную метку: 1, если otvet > 0, иначе -1
            met_otvet = 1 if otvet > 0 else -1
            # Если классификатор ошибся
            if met_otvet != metka[i]:
                # Ошибка между реальной меткой и ответом классификатора
                delta = metka[i] - met_otvet
                # Обновляем веса классификатора
                w[0] += eta * delta
                w[1] += eta * delta * x[i]
                w[2] += eta * delta * y[i]
                # Увеличиваем счётчик ошибок
                count += 1
        # Если за итерацию не было ошибок, возвращаем текущие веса
        if count == 0:
            return w
    # Если обучение не удалось за 10,000 итераций, считаем, что разделяющая линия не найдена
    w[2] = 0
    return w